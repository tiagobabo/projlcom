char *read_xpm(char *map[], int *wd, int *ht)
{
  __attribute__((unused)) static char read_xpm_jcard;

  int width, height, colors;
  char sym[256];
  int  col;
  int i, j;
  char *pix, *pixtmp, *tmp, *line;
  char symbol;

  // read width, height, colors
  if (sscanf(map[0],"%d %d %d", &width, &height, &colors) != 3) {
    printf("read_xpm: incorrect width, height, colors\n");
    return NULL;
  }
#ifdef DEBUG
  printf("%d %d %d\n", width, height, colors);
#endif
  if (width > HRES || height > VRES || colors > 256) {
    printf("read_xpm: incorrect width, height, colors\n");
    return NULL;
  }

  *wd = width;
  *ht = height;

  for (i=0; i<256; i++)
    sym[i] = 0;

  // read symbols <-> colors 
  for (i=0; i<colors; i++) {
    if (sscanf(map[i+1], "%c %d", &symbol, &col) != 2) {
      printf("read_xpm: incorrect symbol, color at line %d\n", i+1);
      return NULL;
    }
#ifdef DEBUG
    printf("%c %d\n", symbol, col);
#endif
    if (col > 256) {
      printf("read_xpm: incorrect color at line %d\n", i+1);
      return NULL;
    }
    sym[col] = symbol;
  }
  
  // allocate pixmap memory
  pix = pixtmp = malloc(width*height);

  // parse each pixmap symbol line
  for (i=0; i<height; i++) {
    line = map[colors+1+i];
#ifdef DEBUG
    printf("\nparsing %s\n", line);
#endif
    for (j=0; j<width; j++) {
      tmp = memchr(sym, line[j], 256);  // find color of each symbol
      if (tmp == NULL) {
	printf("read_xpm: incorrect symbol at line %d, col %d\n", colors+i+1, j);
	return NULL;
      }
      *pixtmp++ = tmp - sym; // pointer arithmetic! back to books :-)
#ifdef DEBUG
      printf("%c:%d ", line[j], tmp-sym);
#endif
    }
  }

  return pix;
}

void drawCharAt(char c, int x_ori, int y_ori, int fore_color, int back_color, int char_scale, char* video_base)
{
	char *char_def = table2 + c*CHR_H; 
	
	int lin, col, sx, sy;
	int x = x_ori;
	int y = y_ori;
	
	for (lin = 0; lin < CHR_H; lin++) {
		for (col = 0; col < 8; col++) {
			if ((*(char_def+lin)) & (1 << (7-col)))
				for (sx = 0; sx<char_scale; sx++)
					for (sy = 0; sy<char_scale; sy++)
						set_pixel(x+sx, y+sy, fore_color, video_base);
			else
				for (sx = 0; sx<char_scale; sx++)
					for (sy = 0; sy<char_scale; sy++)
						set_pixel(x+sx, y+sy, back_color, video_base);
			x += char_scale;
		}
		y += char_scale;
		x = x_ori;
	}
}


void draw_string(char* s, int x_ori, int y_ori, int fore_color, int back_color, int char_scale, char* video_base) {
	int i=0;
	for(i=0; s[i]; i++) {
		drawCharAt(s[i], x_ori, y_ori, fore_color, back_color, char_scale, video_base);
		x_ori += 8*char_scale;
	}
}

Sprite * create_sprite(char *pic[], char *base, int horizontal_x, int vertical_y)
{
	Sprite *spr = (Sprite *)malloc(sizeof(Sprite)); // cria espaço para a Sprite
	
	int width, height;
	
	spr->map = read_xpm(pic, &width, &height); //leitura da figura correspondente à sprite
	
	spr->x = horizontal_x - (int)(width/2);
	spr->y = vertical_y - (int)(height/2);
	
	spr->width = width;
	spr->height = height;
	
	draw_sprite(spr, base);
	
	return spr;
}


void destroy_sprite(Sprite *spr, char *base)
{
	erase_sprite(spr,base);
	// libertar memória
	free(spr->map);
	free(spr);
}

void draw_sprite(Sprite *spr, char *base)
{
	int posY, posX;
	
	for(posY = 0; posY < spr->height; posY++)
		for(posX = 0; posX < spr->width; posX++)
			if(spr->map[posY * spr->width + posX] != BLACK) // so pinta pixels !=s de black
				set_pixel(posX + spr->x, posY + spr->y, spr->map[posY * spr->width + posX], base);
				
}

void erase_sprite(Sprite *spr, char *base)
{
	int i,j,posmap=0;
	//erase_int(spr->x+9, spr->y+17, base);
	// colocar pixels !=s de preto a preto
	for(i = 0; i < spr->height; i++)
		for(j = 0; j < spr->width; j++)
			if(spr->map[posmap++] != BLACK)
				set_pixel(spr->x + j, spr->y + i, BLACK, base);	
	
}

void drawIntAt(int num, int x_ori, int y_ori, int fore_color, int back_color, int char_scale, char* video_base ) {
	int cnt=0, k=num;

	if( num == 0 )
	{
	x_ori=x_ori+8*char_scale*1;
	drawCharAt('0', x_ori, y_ori, fore_color, back_color, char_scale, video_base);
	}
	
	while (k>0) {
		k/=10;
		cnt++;
	}
	
	x_ori=x_ori+8*char_scale*cnt;
	while (cnt>0) {
		cnt--;
		drawCharAt((char)(num%10+'0'), x_ori, y_ori, fore_color, back_color, char_scale, video_base);
		num/=10;
		x_ori-=8*char_scale;
	}
}
